def word_frequency(para):
    dict_word_freq = {}
    #para_broken = para.split(" ")
    #sprint(para)
    
    for word in para:
        if word in dict_word_freq:
            dict_word_freq[word] = dict_word_freq[word] + 1
            
        else:
            dict_word_freq[word] = 1
            
    return dict_word_freq

def common_words(para):
    target_dict = word_frequency(para)
    all_word = [vals for vals in target_dict.values()]
    most_rept_val = max(all_word)
    #print(most_rept_val)
    max_word_count_dict = {}
    
    for k in target_dict:
        if target_dict[k] == most_rept_val:
            max_word_count_dict[k] = target_dict[k]
            
    return max_word_count_dict


